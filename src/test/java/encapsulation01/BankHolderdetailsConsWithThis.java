package encapsulation01;

public class BankHolderdetailsConsWithThis {
	int accountHolder_id;
	String accountHolder_name;
	String accountHolder_address;
	String accountHolder_dob;
	double accountHolder_balance;
	
	public BankHolderdetailsConsWithThis(int accountHolder_id, String accountHolder_name, String accountHolder_address, String accountHolder_dob, double accountHolder_balance) {
		this.accountHolder_id=accountHolder_id;
		this.accountHolder_name=accountHolder_name;
		this.accountHolder_address=accountHolder_address;
		this.accountHolder_dob=accountHolder_dob;
		this.accountHolder_balance=accountHolder_balance;
		
	}

}
